interface ChatMessage {
  role: "system" | "user" | "assistant";
  content: string;
}

interface OpenAIResponse {
  choices: Array<{
    message: ChatMessage;
    finish_reason: string;
  }>;
  error?: {
    message: string;
  };
}

export const aiRouter = createRouter({
  chat: publicQuery
    .input(
      z.object({
        messages: z.array(
          z.object({
            role: z.enum(["user", "assistant"]),
            content: z.string().min(1).max(4000),
          })
        ).max(20),
      })
    )
    .mutation(async ({ input }) => {
      if (!env.aiApiKey) {
        throw new TRPCError({
          code: "INTERNAL_SERVER_ERROR",
          message: "AI сервис временно недоступен. API ключ не настроен.",
        });
      }

      const client = new HttpClient(env.aiBaseUrl, {
        headers: {
          Authorization: `Bearer ${env.aiApiKey}`,
        },
      });

      const apiMessages: ChatMessage[] = [
        { role: "system", content: SYSTEM_PROMPT },
        ...input.messages,
      ];

      try {
        const data = await client.post<OpenAIResponse>("/chat/completions", {
          model: env.aiModel,
          messages: apiMessages,
          temperature: 0.7,
          max_tokens: 1500,
        });

        if (data.error) {
          throw new Error(data.error.message);
        }

        const reply = data.choices[0]?.message?.content;
        if (!reply) {
          throw new Error("Пустой ответ от AI");
        }

        return { reply };
      } catch (error) {
        console.error("AI chat error:", error);
        const message = error instanceof Error ? error.message : "Ошибка при обращении к AI сервису";
        throw new TRPCError({
          code: "INTERNAL_SERVER_ERROR",
          message,
        });
      }
    }),
});
